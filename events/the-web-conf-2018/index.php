            <h2 property="schema:name">Researcher Centric Scholarly Communication</h2>
            <div datatype="rdf:HTML" property="schema:description">
              <p>The purpose of this workshop is the mobilisation of a core group of researchers and practitioners to investigate the core characteristics of a minimal viable platform for scholarly communication that is researcher-centric, interlinked, and web-native. As visible outcomes, this workshop aims to deliver insights regarding these core characteristics and will determine the most appropriate approach to engage in a sustained effort aimed at tackling technical challenges and promoting adoption of solutions.</p>

              <p>The workshop will be held at the <cite><a href="https://www2018.thewebconf.org/">The Web Conf 2018</a></cite> and host invited talks and research presentations. If you would like to share your research at the workshop, respond to the <a href="https://linkedresearch.org/calls">call</a>, and we will ensure your contribution is reviewed accordingly. We encourage all research contributions (articles and reviews) to be part of the <cite><a href="https://linkedresearch.org/cloud">Linked Open Research Cloud</a></cite> (LORC). Check it out!</p>

              <p>Social media hashtag: #LinkedResearch</p>

              <section id="how" rel="schema:hasPart" resource="#how">
                <h3>How?</h3>
                <div datatype="rdf:HTML" property="schema:description">
                  <p>Publish an article on the Web, and send us the URL. You are welcome to share it via <a href="https://gitter.im/linkedresearch/chat">community chat</a>. On <time>2018-02-25</time> we will snapshot the articles with <a href="https://archive.org/">archive.org</a>, and ensure they get at least two reviews each from an <a href="https://linkedresearch.org/calls#reviewers">open committe</a>. Of course, because your article is public, anyone else can give you feedback too. We will let you know if you are invited to present in person by <time>2018-03-11</time>.</p>
                </div>
              </section>

              <section id="what" rel="schema:hasPart" resource="#what">
                <h3>What?</h3>
                <div datatype="rdf:HTML" property="schema:description">
                  <p>The overarching topic for the workshop is web-based scholarly communication and its focus is on discussing how web standards and technologies can be used to achieve a <strong>minimal viable platform</strong> to establish a <a href="https://www.force11.org/scholarly-commons/principles" rel="cito:citesForInformation">scholarly commons</a> that meets the core requirements of a scholarly communication system, i.e. <a href="http://www.physik.uni-oldenburg.de/conferences/crisp97/roosendaal.html" rel="cito:citesForInformation">registration, awareness, certification, and archiving</a>, and that is accessible to both humans and machines.</p>
                </div>
              </section>

              <section id="format" rel="schema:hasPart" resource="#format">
                <h3>Format</h3>
                <div datatype="rdf:HTML" property="schema:description">
                  <p>The format of your contribution is up to you to decide how best to convey your message. Use the presentation timing (15 minutes) as a guideline for how much content you want to submit.</p>

                  <p>The workshop will include an open dicussion session. We invite short blog post style responses to <em>“I can’t use the Web to publish my research because...”</em> which we will use to seed the open session.</p>
                </div>
              </section>

